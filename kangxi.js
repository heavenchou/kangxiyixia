// 由文字取得筆畫
// 名字可以超過一個字
function get_name_num(myname)
{
	names = myname.split("");	// 若有多字, 則拆成陣列
	count = 0;
	for(i=0; i<names.length; i++)
	{
		found = 0;
		for(j=1; j<words.length; j++)
		{
			if(words[j].indexOf(names[i])>=0)
			{
				count += j;
				found = 1;
			}
		}
		if(found == 0) return 0;	// 一遇到找不到的字就傳回 0
	}
	return count;
}

