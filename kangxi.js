// 計算物件的位置及大小
// 若是相對在 div 中, 最後要傳入 1 , 就不用做移位了
function object_location(obj,left,top,width,Height,indiv)
{
	indiv = indiv || 0;	// 若沒傳入則為 0
	
	if(indiv==1)
	{
		$(obj).css("left", left * my_rate);
		$(obj).css("top", top * my_rate);
	}
	else
	{
		$(obj).css("left", left * my_rate + my_left);
		$(obj).css("top", top * my_rate + my_top);
	}
	$(obj).width(width * my_rate);
	$(obj).height(Height * my_rate);
}

// readme 的 size 比較不同, 所以要重新計算

function readme_resize()
{
	// 主視窗大小
	win_height = $(window).height();
	win_width = $(window).width();
	
	// 計算實際容納畫面的大小
	if((win_width / win_height) >= (1920 / 1080))	// 更寬的螢幕
	{
		new_height = win_height;	// 高度設為最高
		new_width = new_height * 1920 / 1080;	// 算出寬度
		new_top = 0;
		new_left = (win_width - new_width) / 2
	}
	else
	{
		// 更長的螢幕
		new_width = win_width;	// 寬度設為最寬
		new_height = new_width * 1080 / 1920;	// 算出高度
		new_left = 0;
		new_top = (win_height - new_height) / 2		
	}
	new_rate = new_width / 1920;	// 這是本畫面與原圖的比率

	$("#page15_main").css("left", 0);
	$("#page15_main").css("top", 0);
	$("#page15_main").width(win_width);
	$("#page15_main").height(win_height);
	
	readme_object_location("#page15",0,0,1920,1080,new_rate,new_left,new_top);
	readme_object_location("#readme_home",937,19,45,45,new_rate,new_left,new_top);
	readme_object_location("#readme_pre",106,517,45,45,new_rate,new_left,new_top);
	readme_object_location("#readme_next",1779,517,45,45,new_rate,new_left,new_top);
	$("#page15").css("background-size" , 1920 * new_rate);
	$("#readme_home").css("background-size" , 45 * new_rate);
	$("#readme_pre").css("background-size" , 45 * new_rate);
	$("#readme_next").css("background-size" , 45 * new_rate);
}

